using System;

namespace CodeNet27.Sesion01.Ejercicio05
{
    class Program
    {
        //Recorre los números del 1 al 100. Usa un bucle for, pintarlos por consola.

        static void Main(string[] args)
        {
            for (int i = 1; i < 101; i++)
            {
                Console.WriteLine(i);
            }

            Console.ReadKey();
        }
    }
}
